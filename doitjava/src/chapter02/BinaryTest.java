package chapter02;

public class BinaryTest {

	public static void main(String[] args) {
		int bNum=0B1010;	//2진수	0B
		int dNum=10;		//10진수
		int oNum=012;		//8진수	0
		int hNum=0xA;		//16진수	0x
		
		System.out.println(bNum);
		System.out.println(dNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}

}
