package chapter03;

public class AssignmentEx {
	public static void main(String[] args) {
		int res = 0;
		res += 10;
		System.out.println("res = " + res);
		res -= 5;
		System.out.println("res = " + res);
		res *= 3;
		System.out.println("res = " + res);
		res /= 5;
		System.out.println("res = " + res);
		res %= 3;
		System.out.println("res = " + res);
	}
}
